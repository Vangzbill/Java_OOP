    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        Dokter d = new Dokter();
        d.setNip(Integer.parseInt(txtNip.getText()));
        d.setNamaDokter(txtNamaDokter.getText());
        d.setAlamat(txtAlamat.getText());
        d.setNoHp(txtNoHp.getText());
        d.setPasien(((Pasien) cmbPasien.getSelectedItem()));
        d.setPendaftaran((Pendaftaran) cmbPendaftaran.getSelectedItem());
        d.save();
        txtNip.setText(Integer.toString(d.getNip()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

