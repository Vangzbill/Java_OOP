    public void tampilkanData(){
        String[] kolom = {"nip","nama_dokter","alamat","noHp","nama","keluhan"};
        ArrayList<Dokter> list = new Dokter().getAll();
        Object rowData[] = new Object[6];
        
        tblDokter.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(Dokter d : list){
            rowData[0] = d.getNip();
            rowData[1] = d.getNamaDokter();
            rowData[2] = d.getAlamat();
            rowData[3] = d.getNoHp();
            rowData[4] = d.getPasien().getNama();
            rowData[5] = d.getPasien().getKeluhan();
            
            ((DefaultTableModel)tblDokter.getModel()).addRow(rowData);
        }
        cmbPasien.setModel(new DefaultComboBoxModel(new Pasien().getAll().toArray()));
        cmbPendaftaran.setModel(new DefaultComboBoxModel(new Pendaftaran().getAll().toArray()));
    }

